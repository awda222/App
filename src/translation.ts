// Normalize input strings to make them matching resilient
export function getTranslation(englishText: string, currentLanguage: string): string {
  if (!englishText) return "";
  if (currentLanguage !== "Hindi") return englishText;

  // Let's do exact phrase look up
  const cleanedKey = englishText.trim();
  const lowerKey = cleanedKey.toLowerCase();

  // Try exact lower case match
  if (HINDI_DICT[cleanedKey]) {
    return HINDI_DICT[cleanedKey];
  }
  if (HINDI_DICT[lowerKey]) {
    return HINDI_DICT[lowerKey];
  }

  // Fallback match parts if possible or return original english text
  // To keep it clean, if no exact mapping, we return the string.
  return englishText;
}
